    public void modifyLabel() {
        TransferHandler th = new TransferHandler() {

            @Override
            public boolean canImport(JComponent comp, DataFlavor[] transferFlavors) {
                return true;
            }

            @Override
            public boolean importData(JComponent comp, Transferable t) {

                try {
                    List<File> file = (List<File>) t.getTransferData(DataFlavor.javaFileListFlavor);
                    rutaInput = file.get(0).getAbsolutePath();
                    jTextField2.setText(rutaInput);
                } catch (UnsupportedFlavorException | IOException ex) {
                    Logger.getLogger(Vuelos.class.getName()).log(Level.SEVERE, null, ex);
                }
                return false;
            }

        };
        DragNdrop.setTransferHandler(th);
    }

