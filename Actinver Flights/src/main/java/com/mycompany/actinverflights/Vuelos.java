    public void clickDatos(WebDriver driver,WebDriverWait wait,String pasajeros,String clase){
        // SELECCIONAMOS LA CLASE
        WebElement claseButton = driver.findElements(By.xpath("//button[@class='VfPpkd-LgbsSe VfPpkd-LgbsSe-OWXEXe-INsAgc VfPpkd-LgbsSe-OWXEXe-dgl2Hf Rj2Mlf OLiIxf PDpWxe BobFtf']")).get(2);
        claseButton.click();

        WebElement claseOptions = wait.until(ExpectedConditions.elementToBeClickable(By.xpath("//div[@class='A8nfpe yRXJAe iWO5td']")));
        WebElement claseOption = claseOptions.findElement(By.xpath("//li[text()='" + clase + "']"));
        claseOption.click();

        // SELECCIONAMOS EL NUMERO DE PASAJEROS
        //Damos click en el boton pasajeros
        WebElement pasajerosButton = driver.findElements(By.xpath("//button[@class='VfPpkd-LgbsSe VfPpkd-LgbsSe-OWXEXe-INsAgc VfPpkd-LgbsSe-OWXEXe-dgl2Hf Rj2Mlf OLiIxf PDpWxe BobFtf']")).get(1);
        pasajerosButton.click();

        //Damos click en el boton de adultos
        WebElement tabla = wait.until(ExpectedConditions.elementToBeClickable(By.xpath("//ul[@class='Xdzhob Lxea9c']")));
        WebElement numPasajerosMas = tabla.findElements(By.xpath("//button[@class='VfPpkd-Bz112c-LgbsSe yHy1rc eT1oJ rGF6nb']")).get(1);
        WebElement numPasajerosMenos = tabla.findElements(By.xpath("//button[@class='VfPpkd-Bz112c-LgbsSe yHy1rc eT1oJ rGF6nb']")).get(0);
        
        WebElement numPasajerosSpan = tabla.findElement(By.xpath("//span[@jsname='NnAfwf']"));
        
        //Verificamos si el numero de pasajeros ya es igual al numero de pasajeros que queremos
        if(!numPasajerosSpan.getText().equals(pasajeros)) {

            //Si es menor damos click en más hasta que sea igual
                while (Integer.parseInt(numPasajerosSpan.getText()) < Integer.parseInt(pasajeros)) {
                    numPasajerosMas.click();
                }
            //Si es mayor damos clic en menos hasta que sea igual
                while(Integer.parseInt(numPasajerosSpan.getText()) > Integer.parseInt(pasajeros)) {
                    numPasajerosMenos.click();
                }

        }
        //Damos click en listo
        WebElement listoButton = wait.until(ExpectedConditions.elementToBeClickable(By.xpath("//button[@class='VfPpkd-LgbsSe ksBjEc lKxP2d bRx3h']")));
        listoButton.click();
    }

    public List<List<String>> getData() throws FileNotFoundException, IOException {
        /*
        Regresa la lista de ciudades dependiendo el indice
        ciudades de partida
        ciudades de llegad
        numero de pasajeros
        clase
         */
        BufferedReader archivo = new BufferedReader(new FileReader(rutaInput));
        List<String> ciudadesPartida = new ArrayList<>();
        List<String> ciudadesLlegada = new ArrayList<>();
        List<String> numPasajeros = new ArrayList<>();
        List<String> clase = new ArrayList<>();
        String line = archivo.readLine();
        while (line != null) {
            String[] camposCsv = line.split(",");
            ciudadesPartida.add(camposCsv[0]);
            ciudadesLlegada.add(camposCsv[1]);
            numPasajeros.add(camposCsv[2]);
            clase.add(camposCsv[3]);
            line = archivo.readLine();
        }

        List<List<String>> datos = new ArrayList<>();
        datos.add(ciudadesPartida);
        datos.add(ciudadesLlegada);
        datos.add(numPasajeros);
        datos.add(clase);        

        return datos;

    }

