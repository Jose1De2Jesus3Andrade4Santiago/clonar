    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        boolean alimentos = false;
        boolean noAlimentos = false;
        if (jCheckBox1.isSelected()) {
            alimentos = true;
        }
        if (jCheckBox4.isSelected()) {
            noAlimentos = true;
        }
        Browser navegador = new Browser(this.ruta, this.rutaDriver, this.idNavegador, alimentos, noAlimentos);
        if (!this.ruta.equals("")) {
            try {
                navegador.navegar();
            } catch (IOException | ParseException | InterruptedException ex) {
                Logger.getLogger(DownloadSeries.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            ErrorWindow ventana = new ErrorWindow();
            ventana.getjLabel1().setText("No ha ingresado una ruta de guardado a√∫n");
            ventana.setVisible(true);
        }
    }//GEN-LAST:event_jButton5ActionPerformed

